    {
        // 設定預設值
        $marker = $request->marker==null ? 1:$request->marker;
        $limit = $request->limit==null ? 10:$request->limit;

        $contact_infos = Space_contact_info::orderBy('id', 'asc')
            ->where('id', '>=', $marker)
            ->where('enable', '=', '1')
            ->limit($limit)
            ->get();

        return response(['items' => $contact_infos], Response::HTTP_OK);
    }

